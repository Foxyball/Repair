    private void btnAddUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddUserActionPerformed
      
        String name = txtUserName.getText();
        String email = txtUserEmail.getText();
        String password = generateTemporaryPassword();
        String phone = txtUserPhone.getText();
        String role = comboUserRole.getSelectedItem().toString();
        String city = txtUserCity.getText();
        String status = comboUserStatus.getSelectedItem().toString();
        String egn = txtUserEGN.getText();

        if (name.isEmpty() || email.isEmpty() || password.isEmpty() || phone.isEmpty() || role.isEmpty() || city.isEmpty() || egn.isEmpty() || txtUserPKOD.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Моля, попълнете всички полета", "Грешка", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Валидация на пощенски код
        int pkod;
        try {
            pkod = Integer.parseInt(txtUserPKOD.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Невалиден пощенски код", "Грешка", JOptionPane.ERROR_MESSAGE);
            return;
        }

// Полета за фирмата
      int is_firm = is_firm_checkbox.isSelected() ? 1 : 0;
        String firm_name = is_firm == 1 ? firm_name_txt.getText() : "";
        String firm_eik = is_firm == 1 ? firm_eik_txt.getText() : "";
        String firm_mol = is_firm == 1 ? firm_mol_txt.getText() : "";
        String firm_dds = is_firm == 1 ? firm_dds_txt.getText() : "";
        String firm_address = is_firm == 1 ? firm_address_txt.getText() : "";

        // Преименуване на ролята
        if (role.equals("Клиент")) {
            role = "user";
        } else if (role.equals("Администратор")) {
            role = "admin";
        } else {
            JOptionPane.showMessageDialog(this, "Невалидна роля", "Грешка", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Преименуване на статус
        if (status.equals("Активен")) {
            status = "active";
        } else if (status.equals("Неактивен")) {
            status = "inactive";
        } else {
            JOptionPane.showMessageDialog(this, "Невалиден статус", "Грешка", JOptionPane.ERROR_MESSAGE);
        }

        String[] columns = {
            "name", "password", "email", "phone", "role", "pkod", "city",
            "status", "egn", "is_firm"
        };

        Object[] values = {
            name, password, email, phone, role, pkod, city, status,
            egn,is_firm
        };

        // Добавяне на фирмени данни
        if (is_firm == 1) {
            columns = new String[]{
                "name", "password", "email", "phone", "role", "pkod", "city",
                "status", "egn", "is_firm", "firm_name", "firm_eik", "firm_mol", "firm_dds", "firm_address"
            };

            values = new Object[]{
                name, password, email, phone, role, pkod, city, status,
                egn, is_firm, firm_name, firm_eik, firm_mol, firm_dds, firm_address
            };
        }

        boolean success = q.insert("users", columns, values);

        String subject = "Вашата временна парола";
        String message = "Здравейте " + name + ",\n\nВашата временна парола е: " + password + "\n\nМоля, променете я след влизане в системата.";

        // Изпращане на имейл
        sendEmail(email, subject, message);

        if (success) {
            JOptionPane.showMessageDialog(this, "Успешно добавяне!" + "\n" + "Временна парола: " + password);
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Възникна грешка при добавянето на потребителя.", "Грешка", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddUserActionPerformed

